// Let's Start with ObjectValues Method in Js

//   The Object.values() static method returns an array of a given object's own enumerable string-keyed property values.

// Syntax
 //Object.values(ObjectName);


 // Example 

 const Obj = {
    a:'Name',
    b: 234,
    C: true
 };

 console.log(Object.values(Obj));   // We got an New Array Which is values of given object = [ 'Name', 234, true ]
